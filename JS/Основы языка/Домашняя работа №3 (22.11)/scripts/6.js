//Задание 6. Создайте функцию avg() , которая будет находить среднее значение по всем своим аргументам (аргументы величины числовые).
			
function avg(numbers) {
    let total = 0
    
    for (number of numbers) {
        total += number;
    };
    console.log(total / numbers.length);
};
avg([2,2,10]);